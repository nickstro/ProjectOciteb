class Conversor {
  static getMenuId(menuId: any) {
    let menu = "";
    switch (menuId) {
      case 1:
        menu = "I01";
        break;
      case 2:
        menu = "I02";
        break;
      case 3:
        menu = "I03";
        break;
      case 4:
        menu = "I04";
        break;
      case 5:
        menu = "I05";
        break;
      case 6:
        menu = "I06";
        break;
      case 7:
        menu = "F01";
        break;
      case 8:
        menu = "F02";
        break;
      case 9:
        menu = "F03";
        break;
      case 10:
        menu = "C01";
        break;
      case 11:
        menu = "C02";
        break;
      case 12:
        menu = "C02.1";
        break;
      case 13:
        menu = "PB03";
        break;
    }
  }
}
